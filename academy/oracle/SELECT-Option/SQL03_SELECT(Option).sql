--10.
SELECT STUDENT_NO AS 학번,STUDENT_NAME AS "학생 이름", ROUND(AVG(POINT),1) AS "전체 평점"
FROM TB_STUDENT
JOIN TB_GRADE USING (STUDENT_NO)
JOIN TB_DEPARTMENT USING (DEPARTMENT_NO)
WHERE DEPARTMENT_NAME='음악학과'
GROUP BY STUDENT_NO,STUDENT_NAME
ORDER BY 1;

--11.
SELECT DEPARTMENT_NAME AS 학과이름,STUDENT_NAME AS "학생 이름", PROFESSOR_NAME AS 지도교수이름
FROM TB_STUDENT
JOIN TB_DEPARTMENT USING(DEPARTMENT_NO)
JOIN TB_PROFESSOR ON COACH_PROFESSOR_NO = PROFESSOR_NO
WHERE STUDENT_NO='A313047';


--12.
SELECT STUDENT_NAME, TERM_NO
FROM TB_STUDENT
JOIN TB_GRADE USING (STUDENT_NO)
JOIN TB_CLASS USING (CLASS_NO)
WHERE CLASS_NAME='인간관계론' AND SUBSTR(TERM_NO,1,4)=2007
ORDER BY 1;


--13.
SELECT CLASS_NAME, DEPARTMENT_NAME
FROM TB_CLASS
JOIN TB_DEPARTMENT USING(DEPARTMENT_NO)
LEFT JOIN TB_CLASS_PROFESSOR USING(CLASS_NO)
WHERE CATEGORY='예체능' AND PROFESSOR_NO IS NULL
ORDER BY 2;

--14.
SELECT STUDENT_NAME AS 학생이름, NVL(PROFESSOR_NAME,'지도교수 미지정') AS 지도교수
FROM TB_STUDENT
JOIN TB_DEPARTMENT USING(DEPARTMENT_NO)
RIGHT JOIN TB_PROFESSOR ON COACH_PROFESSOR_NO=PROFESSOR_NO
WHERE DEPARTMENT_NAME='서반아어학과'
ORDER BY STUDENT_NO;

--15.
SELECT STUDENT_NO AS 학번, STUDENT_NAME AS 이름, DEPARTMENT_NAME AS "학과 이름", AVG(POINT) AS 평점
FROM TB_STUDENT
JOIN TB_GRADE USING(STUDENT_NO)
JOIN TB_DEPARTMENT USING(DEPARTMENT_NO)
WHERE ABSENCE_YN='N'
HAVING AVG(POINT) >= 4.0
GROUP BY STUDENT_NO, STUDENT_NAME, DEPARTMENT_NAME
ORDER BY 1;


--16.
SELECT CLASS_NO, CLASS_NAME, TRUNC(AVG(POINT),8)
FROM TB_CLASS
JOIN TB_GRADE USING(CLASS_NO)
JOIN TB_DEPARTMENT USING(DEPARTMENT_NO)
WHERE DEPARTMENT_NAME='환경조경학과' AND SUBSTR(CLASS_TYPE,1,2)='전공'
GROUP BY CLASS_NO, CLASS_NAME
ORDER BY 1;


