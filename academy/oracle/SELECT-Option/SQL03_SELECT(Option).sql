FROM TB_STUDENT
WHERE SUBSTR(STUDENT_NO,1,1)='9' AND SUBSTR(STUDENT_ADDRESS,1,3)='경기도' OR SUBSTR(STUDENT_ADDRESS,1,3)='강원도';

