SELECT MAX(AVG(POINT)) FROM TB_GRADE GROUP BY STUDENT_NO; -- 제일 높은 평점
SELECT DEPARTMENT_NAME FROM TB_DEPARTMENT WHERE DEPARTMENT_NAME='국어국문학과'; --국어국문학과 

